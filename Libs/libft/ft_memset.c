#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	char		*n;
	size_t		i;

	n = b;
	i = 0;
	while (i < len)
	{
		*n = (char)c;
		n++;
		i++;
	}
	return (b);
}
