#include "libft.h"

void	ft_lstaddend(t_list **alst, t_list *new)
{
	t_list		*tmp;

	if (!new)
		return ;
	if (!*alst)
	{
		*alst = new;
		return ;
	}
	tmp = *alst;
	while (tmp->next)
		tmp = tmp->next;
	tmp->next = new;
}
